<article class="ka">
							<h2>Pannekaker</h2>
<p>
Én pannekake inneholder litt over 100 kcal. Disse pannekakene 
blir mye mektigere enn de laget kun av hvetemel og melk. Det er 
fordi de inneholder mye mer proteiner og fiber, som metter. Beregn 
derfor færre pannekaker til hver person enn du vanligvis ville gjort. 
2-3 pannekaker per person er som regel nok.<br><br>

Ingredienser (2-3 personer/8 pannekaker):<br><br>

3 egg<br>
1 dl havremel (havregryn i stavmixer)<br>
1 dl melk (lett/helmelk gjør at røra blir lettere å snu i panna)<br>
1 boks cottage cheese<br>
1/4 ts salt<br>
1 ss smør (kan sløyfes, men gjør røren lettere å steke)<br>
(evt. 1 ts vaniljesukker)<br>

<ol id="pannekaker">
<li> Visp sammen egg, melk og cottage cheese. Det blir litt klumper av cc så du </li>
kan godt bruke stavmixer for å få røren jevnere<br><br>
<li> Tilsett de tørre ingrediensene og rør inn smeltet smør til slutt</li><br><br>
<li>La røren svelle tildekt i 30 minutter</li><br><br>
<li>Stek pannekakene på middels varme. Helst i teflonpanne fordi de er ganske skjøre.</li><br>
</ol>
</p>
