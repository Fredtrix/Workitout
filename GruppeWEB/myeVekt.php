						<h2>Tips for å gå opp i vekt! </h2>

<p>
For å gå opp i vekt må du ha et energioverskudd på 500–3000 kalorier per dag avhengig av hvor aktiv du er.
Det viktigste er at du innarbeider rutiner som gir gradvis og sikker vektøkning.

<br> <br>
Unngå stress
Stress er kanskje den største trusselen mot å legge på seg. 
Det er derfor du bør legge inn faste rutiner i hverdagen.
Det er også viktig å få nok protein. Men et inntak utover behovet medfører ikke ytterligere vektøkning. 
Et økt kaloriinntak generelt gir mer karbohydrater og energi, 
og nok protein til å sikre optimal protein- og muskeloppbygging i kroppen.
Her kommer en rekke tips som kan hjelpe det opp i vekt. Det viktigste er at du spiser ofte nok og regelmessig.
8 nyttige mattips:

<br> <br>
1) Spis flere måltider
Spis 5–7 ganger daglig. Ikke la det gå mer enn tre timer mellom hvert måltid (bruk gjerne klokken).
 Unngå å bli sulten, spis før sulten kommer og spis mer enn du føler for. Spis stor frokost og kveldsmat.

<br> <br>
2) Sørg for at proteinbehovet dekkes i alle måltider
Velg proteiner fra egg, melkeprodukter (ikke H-melk), ost, fisk, fjærkre og kjøtt. 
Torskefilet og kyllingfilet inneholder mer protein enn fete proteinkilder som pølser og kjøttkaker.
<br> <br>
3) Øk karbohydratinntaket
Spis gjerne karbohydratrik snacks mellom måltidene, som sandwicher, sportsbarer, banan og kjeks.
 Bruk syltetøy og honning for å øke kaloritettheten i måltidene. Fruktjuice, saft og nektar gir mye karbohydrater.
 Drikk gjerne smoothies (melkedrikker med frukt og sukker),
 sjokolademelk og andre energirike drikker til mellommåltidene.
<br> <br>
4) Øk inntaket av riktig fett
Velg fettkilder med umettet fett, for eksempel fet fisk, myk margarin, nøtter, mandler, avokado og oljer.
 Ikke øk inntaket av fete meieriprodukter (H-melk, meierismør, seterrømme m.m.), 
 kjøttprodukter eller snacks (chips, iskrem og lignende) som gir mye mettet fett.
<br> <br>

5) Velg mat som reduserer metthethetsfølelsen

Reduser fiberinntaket, fordi fiber øker metthetsfølelsen. 
Bytt ut noe av grovbrødet med hvitt brød eller loff. Reduser 
væskeinntaket rett før og til måltidene, fordi væske inneholder 
mindre energi enn mat og øker metthetsfølelsen. Drikk heller etter 
at du har spist.
<br> <br>

6) Unngå lettprodukter og tomme kalorier
Unngå produkter med redusert karbohydrat- eller fettinnhold og sukkerfrie produkter, hermetisk frukt og lignende.
<br> <br>

7) Dersom du ikke har jobb i kantine ...
I stedet for matpakke, ta pålegg og brød med på jobben og smør lunsjmaten der. Da blir lunsjen mer fristende.
Enda bedre er det å lage en ekstra middag dagen i forveien og varme den opp igjen på jobben til lunsj. 
Har du ikke muligheter for å varme opp mat på jobben, kan du gjøre dette hjemme og ha maten,
for eksempel en gryterett, på termos.
<br> <br>

8) Gjør mat til en hobby
Kjøp inn noen gode kokebøker eller 
abonner på nyhetsmailer med matoppskrifter. Du kan godt prøve å lage et
 kostholdsprogram ut fra de ovennevnte tipsene. Da skal du ha som mål å legge på deg 0,5–1 kilo i uka.
