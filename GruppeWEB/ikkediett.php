<h3>Størrelse null-pillen</h3>
Denne dietten er blitt brukt av mange radmagre jenter i Hollywood, og består kun av å ta en pillen Clenbuterol.
Clenbuterol er opprinnelig en astmamedisin brukt på hester, og er ikke regnet for bruk på mennesker. Den reduserer matlysten og gjør deg så oppspilt at du har vanskelig for å sitte i ro. Dette gjør at du forforbrenner flere kalorier, og spiser mindre.
Pillen fører til endret hjerterytme, høyt blodtrykk og endret saltbalanse i blodet.
– Denne pillen kan ha livstruende bivirkninger, og jeg ber alle om å tenke seg om og ikke bruke den, sier Steinar Madsen.
