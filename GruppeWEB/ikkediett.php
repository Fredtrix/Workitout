<h3> null USUNT </h3>
Det er ikke sunt å leve på røyk og kaffe.
Franske Legionær-frokost
Denne dietten har fått navnet sitt fra De Franske legionærer.
Frokosten til disse soldatene består av å løpe åtte kilometer daglig, for deretter å fylle på med svart kaffe og sigaretter resten av dagen, og det er nettopp det dietten går ut på også.
– Det er ikke nødvendigvis feil ikke å spise frokost før joggeturen, men om man ikke får i seg annet enn røyk og kaffe hele dagen, er det ikke sunt, sier Cathrine Borchsenius.

