<h3> Luftdietten </h3>
Dietten heter Breatharianism på engelsk, og har mange tilhengere verden over.
Som navnet tilsier består denne dietten enkelt og greit av luft, og ingenting annet. Den eneste forutsetningen er at luften du puster inn ikke må være forurenset.
Håndfast mat er strengt forbudt, men hjemmesiden deres sier ingenting om å drikke vann.
En 49 år gammel skotsk kvinne skal ha blitt funnet død etter at hun fulgte dietten, mens en mann skal ha havnet i koma før han døde av dietten.
– Hvis du bare skal leve på luft, og ikke drikke, vil du dø innen noen dager. Har man lov til å drikke vann kan man klare seg over noe lengre tid, men det er ikke sunt, sier ernæringsekspert Cathrine Borchsenius.

