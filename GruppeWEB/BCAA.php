								<h2>BCAA</h2>
<p>
<h3>Hva er BCAA?</h3>

BCAA står for branched chained amino acids eller forgrenede
aminosyrer som det heter på norsk og består av de 3 
aminosyrene leucine, isoleucine og valine. Disse er de 3 
mest anabole (oppbyggende) aminosyrene som vi har og av disse 3, så er 
leucine den som har vist seg å best stimulere proteinsyntesen.Man finner 
rikelige mengder av BCAA i for eksempel kjøtt og myseprotein. <br><br>

<h3>BCAA og proteinsyntesen<br></h3>

Proteinsyntesen er prosessen hvor proteiner dannes. I kroppen fraktes aminosyrene fra blodet og inne i cellene våre. Prosessen i proteinsyntesen går fra DNA via RNA til protein. Når proteinsyntesen stimuleres og prosessen øker i omfang vil det bidra til muskelvekst (hypertrofi) og det er derfor en viktig del av prosessen i å få en muskel til å vokse.

Enkelt forklart så vil muskelvekst forekomme hvis proteinsyntesen er økt iforhold 
til muskelnedbrytning. Dette vil skape det som kalles en positiv muskelproteinbalanse.

Muskel-protein syntese > muskel-protein nedbrytning
= positiv muskel-proteinbalanse = økning i muskelmasse

Leucine som er en av aminosyrene som inngår i BCAA har vist seg i en rekke 
forsøk og studier å kunne akvitere en essensiell prosess i muskelbygging som 
heter mTOR ( mammalian target of rapamycin). mTOR funger som et molekyl som 
skrur ”av og på” en bryter for å lage muskelprotein og leucine viser seg å 
være en svært sterk aktivator av mTOR.

I en studie utført av Koopman med flere, 
trente deltakerne styrketrening i 45 minutter 
før de inntok en restitusjonsdrikk. Den ene gruppen 
inntok kun karbohydrater. Den andre gruppen inntok både karbohydrater og 
proteiner, mens den siste gruppen inntok karbohydrater, proteiner og i tillegg tilskudd av 
leucine. Sistnevnte gruppe reduserte muskelnedbrytningen samt økte proteinsyntesen i mye større 
grad enn de to andre gruppene.<br><br>

<h3>BCAA og kroppsammensetning<br></h3>

En studie av Qin med flere, så de på 
sammenhengen på inntak av BCAA og forekomst av overvekt. 
Studien hadde totalt 4429 deltakere og det ble påvist at 
deltakerne i studien som hadde et høyere inntak av BCAA hadde 
en mye lavere fettprosent og bedre kroppsammensetning. I tillegg 
var det en redusert risiko for å utvikle overvekt blant disse deltakerne.<br><br>

<h3>BCAA og utholdenhet<br></h3>

BCAA har vist seg å ha svært god effekt hos utholdenhetsutøvere i flere studier. En studie utført av Pelter med flere viste det seg at gruppen som inntok BCAA og koffein hadde en 2 % økning av prestasjoner under en 120 minutters løpeøkt på tredemølle imotsetning til placebo.

Mye kan derfor tyde på at BCAA, koffein og karbohydrater kombinert 
med hverandre kan være effektivt for å prestere bedre hos utholdenhetsutøvere.<br><br>

<h3>BCAA og muskelstølhet<br></h3>

BCAA har også vist seg i studier til å bedre restitusjon etter trening ved å forhindre muskelstølhet og da spesielt DOMS (Delayed onset musclesoreness). Dette er en form for stølhet som kommer 1-2 dager etter at man har fullført treningsøkten. Jo fortere du klarer å restituere deg fra trening, jo fortere kan du trene samme muskel igjen. Studier viser at jo oftere du trener en muskel, jo større er potensiale for at den vil øke i tversnitt, forutsatt at du ikke overtrener.

En studie utført av Shimomura med 
flere ønsket de å se på DOMS ved inntak 
av BCAA eller placebo før trening. Det var 
12 utrente kvinner som deltok i studien. Deltakerne
 skulle før de trente knebøy innta 100 mg/kg med BCAA 
 (blandingsforhold isoleucine:leucine:valine = 1:2.3:1.2) eller dextrin. 
 DOMS nådde sitt toppunkt 2-3 dager etter treningsøkten, men ved inntak 
 av BCAA var deltakerne mye mindre støle enn ved inntak av dextrin.<br><br>

<h3>BCAA og hormonbalanse<br></h3>

BCAA kan bidra til å redusere det muskelnedbrytende hormonet kortisol, samtidig som det optimaliserer testosteronverdiene. Dette gir en bedre hormonbalanse for optimalisering av muskelvekst.

En randomisert dobbel-blind placebo-kontrollert studie ble utført av Hsu med flere hvor de ønsket å se på hormonresponsen hos deltakere etter inntak av en blanding som bestod av BCAA, arginine og karbohydrater eller placebo.

Testosteronverdiene til deltakerne 
i BCAA gruppen var signifikant høyere, 
samt at ratioen mellom kortisol og testosteron var i favør av sistnevnte.
