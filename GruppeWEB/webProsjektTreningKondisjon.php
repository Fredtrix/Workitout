<h2>Kondisjon</h2>
<p>Å trene kondisjon er veldig viktig! Det gir bedre energi og du vil føle deg mye bedre. Hverdagslige ting som kanskje kan være litt slitsomt blir 
kanskje ikke så tungt lengere. Det kommer mye godt med kondisjonstrening, så knyt på deg joggeskoa og kom deg ut!  
Den enkleste oppskriften på å få bedre kondis er å bevege kroppen så mye at du oppnår høy puls under en treningsøkt.
Det er mange måter å gjøre dette på men de mest tradisjonelle metodene er løping, sykling og svømming.
Er du usikker på hvordan du skal legge opp en uke med disse metodene? Ta en titt under her.</p>

<img class="mot" src="motivasjon.jpg" alt="motivasjon" height="400" width="830">

<h3>Nybegynner</h3>

<h4>Dag 1 (f.eks. Mandag)</h4>
<p>20 min. gange eller løping 
fordelt på rask gange i 5 minutter og deretter jogg i 1 minutt. 
20 min til sammen</p><br>

<h4>Dag 2 (f.eks. Onsdag)</h4>
<p>20 min. gange eller løping 
Rask gange; hvert 5. minutt jogger du 1 minutt. 
20 min til sammen</p><br>

<h4>Dag 3 (f.eks. Lørdag)</h4>
<p>30 min. rask gange 
Rask gange; hvert 5. minutt jogger du 1 minutt. 
30 min til sammen</p><br>

<h3>4x4 intervall</h3>
<ol>
<li>Oppvarmingen bør vare mellom 5 og 10 minutter – og ligge opp mot 70 % av makspuls</li>
<li>Dragene på intervallene kan vare i 3 til 8 minutter</li>
<li>Pausene kan være på 3 til 4 minutter, og ligge på ca 70 % av makspuls</li>
<li>Totalt bør dragene være på mellom 15 og 25 minutter. 5 intervaller på 5min gir 25 min totalt</li>
<li>Intensiteten på intervallene bør være på 85-95 % av makspuls</li>
<li>De første minuttene på dragene vil du ligge på mellom 85-90 %, mens du vil ligge på 90-95 % på de 2 siste (hvis du gjennomfører 4×4)</li>
<li>Løper du intervallene i motbakke (f.eks 5% stigning på tredemølla) vil dette være snillere på beina, samtidig som det blir enklere å få opp intensiteten.</li>
<li>Med 2-3 intervalløkter i uken kan du regne med en fremgang på 
0,5 % pr økt. En økning på kun 3 % vil gi 10slag mindre i minuttet – og tilsvare en reduksjon i biologisk alder på hele 10 år!</li>
</ol>
