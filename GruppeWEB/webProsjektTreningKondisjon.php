<h2>Kondisjon</h2>
<p>Å trene kondisjon er veldig viktig! Det gir bedre energi og du vil føle deg mye bedre. Hverdagslige ting som kanskje kan være litt slitsomt blir 
kanskje ikke så tungt lengere. Det kommer mye godt med kondisjonstrening, så knyt på deg joggeskoa og kom deg ut!  
Den enkleste oppskriften på å få bedre kondis er å bevege kroppen så mye at du oppnår høy puls under en treningsøkt.
Det er mange måter å gjøre dette på men de mest tradisjonelle metodene er løping, sykling og svømming.
Er du usikker på hvordan du skal legge opp en uke med disse metodene? Ta en titt under her.</p>
