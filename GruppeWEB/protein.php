<h2> Proteiner</h2>
<p>					

Proteiner er nødvendige for oppbygning av
 nye celler - både under vekst og til 
vedlikehold av kroppen. Proteiner er store molekyler som er bygd opp av aminosyrer.
​<br>
​Proteiner deltar på forskjellige måter 
i alle livsprosesser i kroppen. Protein 
kan også brukes til energi, men spiller vanligvis 
liten rolle som energikilde i forhold til karbohydrat og fett.
 <br>
Proteinene er bygd opp av aminosyrer. 
I naturen finnes over hundre forskjellige
 aminosyrer. Omtrent 20 er vanlige i dyreorganismer. En del av aminosyrene kan menneskekroppen ikke produsere selv i tilstrekkelige mengder, og de må derfor tilføres via maten. De kalles derfor for essensielle aminosyrer.
 <br><br>
<h3>Anbefalt inntak</h3>
<br>
Det anbefales at proteininntaket utgjør 
10-20 prosent av energiinntaket. En voksen person 
trenger en daglig tilførsel på omtrent 0,8 g protein per kilo
 kroppsvekt. Dette tilsvarer omtrent 8-10 prosent av energiinntaket. Ettersom de fleste matvarer har et proteininnhold som er høyere enn 10 energiprosent, er det vanskelig å sette sammen et kosthold med mindre enn 10 energiprosent protein.
 <br>
Gjennomsnittskostens innhold av protein har økt fra 
12 til 15 prosent av energiinntaket i løpet av de siste 30 årene. Det er dermed på anbefalt nivå.
 <br><br>
<h3>Kilder til proteiner</h3>
<br>
Fisk, kjøtt, melk, ost og egg er rike på proteiner. Vegetabilske matvarer som korn, bønner og nøtter er også viktige proteinkilder.
De største proteinkildene i norsk gjennomsnittskost er kjøtt, kornvarer og meieriprodukter.
 <br><br>
<h3>Proteiner i noen matvarer</h3>
<br>
et glass melk (2 dl) bidrar med 6-7 gram protein.<br>
to skiver ost (20 g) bidrar med 6-7 gram protein<br>
en skive brød (40 g) bidrar med 3-4 gram protein<br>
en porsjon torsk (200 g) bidrar med 36 gram protein<br>
En voksen mann trenger omtrent 60 gram protein per dag.<br>
