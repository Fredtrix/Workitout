<article class="ka">
							<h2> Kreatin</h2>
<p>
Kreatin er et stoff som finnes naturlig i kroppens muskler og som spiller en 
viktig rolle i kroppens energiomsetning. Kroppen danner 1-2 gram kreatin daglig, 
men dette stoffet finnes også i kjøtt og fisk.<br>
I musklene omdannes kreatin til formen
kreatinfosfat som deretter fungerer som en utilgjengelig energikilde ved høyintensiv 
trening.<br><br>
Studier viser at kreatinsupplementering øker prestasjonen under eksplosivt, 
kortvarig arbeid - f.eks. økt maksstyrke og evne til å utføre flere repetisjoner ved 
styrketrening.
 
Ved å innta kreatin som et kosttilskudd kan du øke din muskelstyrke og muskelvekst!
<br><br>
Man må huske på at det er ikke sikkert Kreatin hjelper for akkuratt deg, siden 1/3 av oss har fulle kreatin lagre fra
før å det gjør at det ikke har noe effekt. Dette er fordi hvis du har fulle kreatin lagre naturlig kan du ikke fylle
på noe mere som gjør at du har full effekt fra dette fra før. men det er da 2/3 som ikke har dette fylt opp fra før, å da
vil du merke en forbedring av å bruke dette produktet.
